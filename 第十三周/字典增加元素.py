'''
现有字典dict1 尝试向其中加入一个键值对，键和值在两行里输入，如果输入的键在字典中已经存在，则输出“您输入的姓名在通讯录中已存在”，如输入的键在字典中不存在则把键和值加入到字典中。操作完成后输出字典中所有键值对。
'''

if __name__ == '__main__':
    dict1 = {'赵小明': '13299887777', '特明朗': '814666888',
             '普希京': '522888666', '吴小京': '13999887777'}
    key = input()
    value = input()
    if key in dict1:
        print('您输入的姓名在通讯录中已存在')
    else:
        dict1[key] = value
        for k, v in dict1.items():
            print('{}:{}'.format(k, v))
